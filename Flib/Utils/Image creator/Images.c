// Ce programme sert à transformer le texte renvoyé par un programme de type BMP->C avec quatre niveaux de gris
// en deux images au format 89i, une pour chaque plane, pour pouvoir ensuite être affiché par Flib en gris.

// Je viens de tester à peu près tous les programmes de type BMP->asm
// sur Ticalc/win/graphics. Mon verdict :
//
// - Graphics Studio : très bien pour dessiner des images de n'importe quelle
// taille, et convertit bien certaines images car il a un très bel effet de
// diffusion. Par contre, manque énorme : pas moyen de règler la luminosité au
// moment de la conversion, vous ne pouvez pas imaginer à quel point c'est important
// pour des images en quatre niveaux de gris. De plus, les images doivent avoir des
// tailles qui 'tombent rond' (multiples de 8 en horizontal et vertical, ça finit
// par être ennuyeux).
// - TiPaintPlus : bien, possibilité d'importation et de réglages, exportation possible
// sous de nombreux formats (C entre autres), peut-être un peu lent.
// - Image Studio : une conversion extrèmement simple avec tous les règlages
// nécessaires, mais pas de possibilité de corriger le fichier qu'on importe une
// fois qu'il est converti.
// - TIGCC Sprite Editor : simpliste, pour dessiner des sprites 8, 16, ou 32, mais
// très pratique à l'usage, pas de fonction de conversion de BMP.
//
// Pour certains d'entre eux qui créent des fichiers asm 68K, il faut effectivement
// remplacer tous les $ par des 0x, virer les dc.b en début de ligne, 
// rajouter des virgules à la fin de chaque ligne et des accolades au début et à la fin.
// Ouf ! Mais bon, certains des logiciels
// (tigcc sprite editor) crééent directement des textes pour le C.


// Pour voir s'il n'y a pas d'erreur, le programme affiche le résultat, et attend que l'utilisateur appuie sur une touche.

